# script que pede que meu usuário insira o numero zero(0) para que seja impreso "Fim do
# programa", caso contrário vai continuar funcionando por tempo indeterminnado até que essa
# condiçaõ acima seja verdadeira#

# declarando variável auxiliar para o while, através dessa que todo o sistema vai funcionar e
# ser corretamenta analisado
num = 1
while num != 0:
    num = int(input("Escolha um numero:"))
print("Fim do Programa!")
